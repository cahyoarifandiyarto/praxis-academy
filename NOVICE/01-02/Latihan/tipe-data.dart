main() {
  //Integer
  int x = 1;
  print(x);

  //Double
  double y = 3.14;
  print(y);

  //String
  var name = "Cahyo Arif Andiyarto";
  print(name);

  //Boolean
  bool married = false;
  bool student = true;
  print("$married $student");

  //Array / List
  var hobbies = ["Music", "Futsal"];
  print(hobbies);

  //Set
  var education = {"SD", "SMP", "SMK"};
  print(education);

  //Map
  var biodata = {
    "name" : "Cahyo Arif Andiyarto",
    "age" : 18,
    "address" : "Sleman"
  };
  print(biodata);

}