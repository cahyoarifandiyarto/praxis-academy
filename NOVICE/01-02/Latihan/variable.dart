main() {
  //Declare variable
  var namaDepan = "Cahyo";
  var namaBelakang = "Arif Andiyarto";
  print("$namaDepan $namaBelakang");

  //Declare dynamic
  dynamic fullName = "Cahyo Arif Andiyarto";
  dynamic address = "Sleman";
  print("$fullName $address");

  //Declare String
  String hobbies = "Music";
  String passion = "Programming";
  print("$hobbies $passion");

  //Declare final
  final socialMedia = "cahyoarifandiyartoo";
  print(socialMedia);

  //Declare const
  const age = 18;
  print(age);
}